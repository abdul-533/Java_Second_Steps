package JavaVeriables;

public class ComparisonOperators {
    public static void main(String[] args) {
        int a=50;
        int b=50;

        System.out.println("does a equal b  ? " +(a==b)); //true
        System.out.println("is a different from b? " +(a!=b)); // false
        System.out.println("is a greater than b  ? " +(a>b));   // false
        System.out.println("is a less than b? " +(a<b));  //false
        System.out.println("is a greater than or equal to b? " +(a>=b)); //true
        System.out.println("is a less than b or equal to? " +(a<=b)); //true


    }
}
